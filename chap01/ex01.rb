# ex01.rb

# how many hours are in a year
hours_in_a_years = 365 * 24
puts "The are #{hours_in_a_years} hours in a year!"

# how many minutes are in a decade ?
minutes_in_a_decade = 10 * 365 * 24 * 60
puts "The are #{minutes_in_a_decade} minutes in a decade!"

# how many seconds old are you?
seconds_old = 36 * 365 * 24 * 60 * 60
puts "I am #{seconds_old} seconds old!"

# how many chocolats do you hope to eat in your life?
number_chocolate_in_lifetime = 100 * 365
puts "I hope to eat up to #{number_chocolate_in_lifetime} chocolats in my lifetime!"

# if I am 1230 million seconds old, how old am I?
age = 1230000000 / 60 / 60 / 24 / 365
puts "I am #{age} years old!"